# Step 1: Create a Dictionary
student = {
    "name": "Marwa",
    "age": 20,
    "course": "Python"
}

# Step 2: Accessing elements in the dictionary
print("Name:", student["name"])
print("Age:", student["age"])

# Step 3: Adding new elements to the dictionary
student["grade"] = "A"
student["city"] = "Mumbai"

# Step 4: Display the updated dictionary
print("Updated Dictionary:", student)
