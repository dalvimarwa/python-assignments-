# program to perform following operations on List. Creating a List Accessing Elements Index

# Creating a list
my_list = [10, 20, 30, 40, 50]

# Accessing an element
element = my_list[2]   # Accessing the 3rd element (index 2)

# Finding index of an element
index_value = my_list.index(40)

print("List:", my_list)
print("Accessed Element:", element)
print("Index of 40:", index_value)


